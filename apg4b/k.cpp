#include <bits/stdc++.h>
using namespace std;

int main()
{
    int A, B;
    cin >> A >> B;
    //  graph of A
    int i = 0;
    cout << "A:";
    while (i < A)
    {
        cout << "]";
        i++;
    }
    cout << endl;
    // graph of B
    i = 0;
    cout << "B:";
    while (i < B)
    {
        cout << "]";
        i++;
    }
    cout << endl;
}
